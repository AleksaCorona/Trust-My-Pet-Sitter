
$(function() {
  $('#faqBg').hover(function() {
  $('#spanContainer').css('background', 'rgb(0, 0, 0, 0.6');
  }, function() {
    // on mouseout, reset the background colour
  $('#spanContainer').css('background', '');
  });
});

